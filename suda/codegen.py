# ============================================================================
# C++ Dispatcher Generator
# ============================================================================

def generate_dispatcher_cpp(ops: List[str], multicore: bool = False) -> str:
    """Generate complete dispatcher C++ source with nmpp linkage."""

    lines = []
    lines.append('// ============================================================')
    lines.append('// SUDA-Generated Dispatcher for NMC4 NeuroMatrix')
    lines.append('// Generated by: python suda/codegen.py')
    lines.append('// Vector matmul: nmppmMul_mm_32f (4 FPU cores, rep vlen)')
    lines.append('// ============================================================')
    lines.append('')

    # nmpp declarations
    has_nmpp = any(KERNELS[op].use_nmpp for op in ops)
    if has_nmpp:
        lines.append('extern "C" {')
        lines.append('    void nmppmMul_mm_32f(float* pSrcMtr1, int nHeight1, int nStride1,')
        lines.append('                          float* pSrcMtr2, int nWidth1, int nStride2,')
        lines.append('                          float* pDstMtr, int nWidth2, int nStrideDst, int bPlusDst);')
        lines.append('}')
        lines.append('')

    lines.append('#define DDR_BASE 0x00340000')

    if multicore:
        lines.append('#define CMD_BLOCK_SIZE 32')
        lines.append('volatile unsigned int* mem;')
    else:
        lines.append('volatile unsigned int* mem = (volatile unsigned int*)DDR_BASE;')

    lines.append('')

    # Opcodes
    for op_name in ops:
        k = KERNELS[op_name]
        lines.append(f'#define OP_{op_name.upper():20s} {k.opcode}')
    lines.append(f'#define OP_{"NOP":20s} 0')
    lines.append(f'#define OP_{"EXIT":20s} 255')
    lines.append(f'#define STATUS_ADDR     30')
    lines.append(f'#define WATCHDOG_ADDR   31')
    lines.append('')

    # Operation functions
    for op_name in ops:
        k = KERNELS[op_name]
        lines.append(f'// {op_name}: args [{", ".join(k.args)}]')
        if k.use_nmpp:
            lines.append(f'// VECTORIZED via nmpp ({k.nmpp_func})')
        lines.append(f'void op_{op_name}() {{')
        lines.append(k.body_scalar)
        lines.append('}')
        lines.append('')

    # Main dispatcher loop
    if multicore:
        lines.append('// Stubs for libnm6408load_nmc')
        lines.append('extern "C" unsigned int LShift32(unsigned int a, unsigned int b) {')
        lines.append('    unsigned int r=a; for(unsigned int i=0;i<b;i++) r<<=1; return r; }')
        lines.append('extern "C" unsigned int RShift32(unsigned int a, unsigned int b) {')
        lines.append('    unsigned int r=a; for(unsigned int i=0;i<b;i++) r>>=1; return r; }')
        lines.append('extern "C" int Mul32(int a, int b) {')
        lines.append('    int r=0,s=1; if(a<0){a=-a;s=-s;} if(b<0){b=-b;s=-s;}')
        lines.append('    unsigned int ua=(unsigned int)a,ub=(unsigned int)b;')
        lines.append('    while(ub){if(ub&1)r+=ua;ua<<=1;ub>>=1;} return s<0?-r:r; }')
        lines.append('')
        lines.append('int main() {')
        lines.append('    volatile unsigned int* boot=(volatile unsigned int*)DDR_BASE;')
        lines.append('    unsigned int core_index=boot[29];')
        lines.append('    if(core_index>15) core_index=0;')
        lines.append('    mem=(volatile unsigned int*)(DDR_BASE+(core_index<<5));')
    else:
        lines.append('int main() {')

    lines.append('    mem[STATUS_ADDR]=1; mem[WATCHDOG_ADDR]=0; mem[0]=0;')
    lines.append('    unsigned int watchdog=0;')
    lines.append('    while(1){')
    lines.append('        watchdog++; mem[WATCHDOG_ADDR]=watchdog;')
    lines.append('        unsigned int op=mem[0];')
    lines.append('        if(op==OP_NOP) continue;')
    lines.append('        if(op==OP_EXIT){mem[STATUS_ADDR]=1;mem[0]=0;break;}')
    lines.append('        mem[STATUS_ADDR]=0;')
    lines.append('        switch(op){')
    for op_name in ops:
        k = KERNELS[op_name]
        tag = ' /*VEC*/' if k.use_nmpp else ''
        lines.append(f'            case OP_{op_name.upper()}: op_{op_name}(); break;{tag}')
    lines.append('            default: mem[STATUS_ADDR]=2; mem[0]=0; continue;')
    lines.append('        }')
    lines.append('        mem[STATUS_ADDR]=1; mem[0]=0;')
    lines.append('    }')
    lines.append('    return 0;')
    lines.append('}')

    return '\n'.join(lines) + '\n'


def generate_build_script(name: str, has_nmpp: bool, multicore: bool) -> str:
    """Generate build.bat for the dispatcher."""
    lines = []
    lines.append('@echo off')
    lines.append('set NMC_GCC_TOOLPATH=C:\\Program Files\\Module\\NMC-SDK')
    lines.append('set IDE=%NMC_GCC_TOOLPATH%\\nmc4-ide')
    lines.append('set CC1PLUS=%IDE%\\libexec\\gcc\\nmc\\4.8.3\\cc1plus.exe')
    lines.append('set AS_GNU=%IDE%\\bin\\nmc-as-gnu.exe')
    lines.append('set LD=%IDE%\\bin\\nmc-ld.exe')
    lines.append('set LIBGCC_FLOAT=%IDE%\\lib\\gcc\\nmc\\4.8.3\\nmc4-float\\libgcc.a')
    lines.append('')
    lines.append(f'echo Building {name}.abs (SUDA generated)...')
    lines.append(f'"%CC1PLUS%" -quiet {name}.cpp -mnmc4 -mgas -O2 -o {name}_gas.s')
    lines.append(f'"%AS_GNU%" -o {name}_gas.o {name}_gas.s')

    link_objs = f'nmc_startup.o {name}_gas.o'
    if has_nmpp:
        link_objs += ' MullMatrix_f.o TmpBuffers64G.o'
    link_objs += ' "%LIBGCC_FLOAT%"'

    lines.append(f'"%LD%" -o {name}.abs {link_objs} -T nm6408brd.lds -e start')
    lines.append(f'if exist {name}.abs (')
    lines.append(f'    echo [OK] {name}.abs')
    lines.append(f'    del {name}_gas.s {name}_gas.o 2>nul')
    lines.append(') else (')
    lines.append(f'    echo [FAIL] Build failed')
    lines.append(')')
    return '\n'.join(lines) + '\n'


# ============================================================================
# Main
# ============================================================================

def main():
    parser = argparse.ArgumentParser(description='SUDA Compiler — NMC4 Kernel Code Generator')
    parser.add_argument('--op', default='all', help='Operation to generate (or "all")')
    parser.add_argument('--output', default='dispatcher_suda.cpp', help='Output C++ file')
    parser.add_argument('--multicore', action='store_true', help='Generate 16-core dispatcher')
    parser.add_argument('--build-script', default=None, help='Generate build.bat')
    parser.add_argument('--list-ops', action='store_true', help='List available operations')
    args = parser.parse_args()

    if args.list_ops:
        print('Available SUDA operations:')
        for name, spec in KERNELS.items():
            vec = ' [VECTORIZED nmpp]' if spec.use_nmpp else ''
            print(f'  {name:15s} opcode={spec.opcode:3d}  args={spec.args}{vec}')
        return

    if args.op == 'all':
        ops = list(KERNELS.keys())
    else:
        ops = [o.strip() for o in args.op.split(',')]
        for op in ops:
            if op not in KERNELS:
                print(f'ERROR: Unknown operation "{op}". Use --list-ops to see available.')
                sys.exit(1)

    # Generate C++
    cpp = generate_dispatcher_cpp(ops, multicore=args.multicore)

    output_path = args.output
    if not os.path.isabs(output_path):
        output_path = os.path.join(os.path.dirname(os.path.abspath(__file__)),
                                    '..', 'aten', 'src', 'ATen', 'nmcard', 'nmc_programs',
                                    output_path)

    with open(output_path, 'w') as f:
        f.write(cpp)

    has_nmpp = any(KERNELS[op].use_nmpp for op in ops)
    name = os.path.splitext(os.path.basename(args.output))[0]
    print(f'SUDA: Generated {output_path}')
    print(f'  Operations: {", ".join(ops)}')
    print(f'  Vectorized (nmpp): {", ".join(o for o in ops if KERNELS[o].use_nmpp) or "none"}')
    print(f'  Multicore: {args.multicore}')

    # Generate build script
    build_path = args.build_script
    if build_path is None:
        build_path = os.path.join(os.path.dirname(output_path), f'build_{name}.bat')
    with open(build_path, 'w') as f:
        f.write(generate_build_script(name, has_nmpp, args.multicore))
    print(f'  Build script: {build_path}')

    print(f'\nTo compile:')
    print(f'  cd aten/src/ATen/nmcard/nmc_programs')
    print(f'  build_{name}.bat')


if __name__ == '__main__':
    main()
