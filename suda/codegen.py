"""
SUDA Compiler — NMC4 Kernel Code Generator for PromeTorch
==========================================================
Generates optimized NMC4 assembly (.asm) and C++ dispatcher code
for ML operations on NeuroMatrix vector pipeline.

Usage:
    python suda/codegen.py --op matmul --output dispatcher_suda.cpp
    python suda/codegen.py --op all --output dispatcher_suda.cpp

The generated code links with:
  - libnmpps-nmc4.a (vectorized matmul via 4 FPU cores)
  - libgcc_float.a (IEEE 754 float support)

NMC4 Vector Pipeline ISA (from MullMatrix_f.asm analysis):
  fpu N rep vlen vreg = [arX++grX]           # vector load with stride
  fpu N .matrix vreg = vregA * .retrive(vregB, vregC)           # matrix FMA
  fpu N .matrix vreg = vregA * .retrive(vregB, vregC) + vregD   # matrix FMA+acc
  fpu N vregX = fpu M vregX                  # inter-FPU data transfer
  vlen = grN                                 # set vector length
  rep N <instruction>                        # repeat N times (no branch overhead)
"""

import argparse
