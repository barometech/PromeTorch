import sys
from dataclasses import dataclass
from typing import List, Optional


@dataclass
class KernelSpec:
    """Specification for an NMC4 kernel."""
    name: str           # Operation name
    opcode: int         # Dispatcher opcode
    args: List[str]     # Argument names from mem[] words
    use_nmpp: bool      # Use nmpp vectorized library
    nmpp_func: str      # nmpp function name (if use_nmpp)
    body_scalar: str    # Scalar C fallback body


